package Continue_Break_Statement;

import java.util.*;
public class Retrive_Negative_Number {
	public static void main(String[]args) {
		Scanner sc=new Scanner(System.in);
		while(true) {
			int num=sc.nextInt();
		
		if(num<0) {
			System.out.println("Program Ended");
			break;
		}
	}

}
}

